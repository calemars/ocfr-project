function addRegistration() {
  var x = document.getElementById("registration");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function addCertification() {
  var x = document.getElementById("certification");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
